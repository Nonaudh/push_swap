#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	y;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = n * (-1);
		}
		y = (n % 10) + '0';
		n = n / 10;
		if (n > 0)
			ft_putnbr_fd(n, fd);
		write(fd, &y, 1);
	}
}
